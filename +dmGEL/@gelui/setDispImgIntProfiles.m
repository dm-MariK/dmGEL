function setDispImgIntProfiles(obj, src, ~)
% Callback for: 'View -> Calculation Details -> Image Intensity Profiles'

if strcmpi(get(src, 'Checked'), 'on')
    set(src, 'Checked', 'off');
    obj.DispImgIntProfiles = false;
else
    set(src, 'Checked', 'on')
    obj.DispImgIntProfiles = true;
end
end